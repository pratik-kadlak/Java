public class Variables2 {
    
    // Variables are a way to store information in our computer.

    public static void main(String[] args) {
        int x = 5;
        char ch = 'p';
        System.out.println(x);
        System.out.println(ch);

        // changing value of variable
        x = 10;
        System.out.println(x);
    }


}
